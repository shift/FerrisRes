/// Pipeline for creating, validating, and registering tools from model output.
///
/// The pipeline:
/// 1. Parses tool specifications from model output
/// 2. Validates syntax (WASM sandbox or structural checks)
/// 3. Validates semantics (mirror test or functional checks)
/// 4. Registers passing tools in the tool registry
/// 5. Tracks creation attempts for learning
pub struct ToolCreationPipeline {
    config: ToolCreationConfig,
    /// History of all creation attempts.
    attempts: Vec<CreationAttempt>,
    /// Currently registered created tools (name → spec).
    created_tools: HashMap<String, ToolSpec>,
    /// Counter for unique tool IDs.
    next_id: u64,
}

impl ToolCreationPipeline {
    /// Create a new pipeline with the given configuration.
    pub fn new(config: ToolCreationConfig) -> Self {
        Self {
            config,
            attempts: Vec::new(),
            created_tools: HashMap::new(),
            next_id: 0,
        }
    }

    /// Create with default configuration.
    pub fn default_pipeline() -> Self {
        Self::new(ToolCreationConfig::default())
    }

    /// Get the pipeline configuration.
    pub fn config(&self) -> &ToolCreationConfig {
        &self.config
    }

    /// Number of created tools currently registered.
    pub fn created_count(&self) -> usize {
        self.created_tools.len()
    }

    /// Total creation attempts (successful + failed).
    pub fn total_attempts(&self) -> usize {
        self.attempts.len()
    }

    /// Successful creations.
    pub fn successful_count(&self) -> usize {
        self.attempts.iter().filter(|a| a.success).count()
    }

    /// Get a created tool by name.
    pub fn get_created_tool(&self, name: &str) -> Option<&ToolSpec> {
        self.created_tools.get(name)
    }

    /// List all created tool names.
    pub fn created_tool_names(&self) -> Vec<&str> {
        self.created_tools.keys().map(|s| s.as_str()).collect()
    }

    // ---- Main API ----

    /// Parse and validate a tool creation request from model output.
    ///
    /// Returns `Some(CreationAttempt)` if a [tool_create] block was found,
    /// `None` if no tool creation was requested.
    pub fn process_creation(
        &mut self,
        model_output: &str,
    ) -> Option<&CreationAttempt> {
        let spec = ToolSpec::parse_from_output(model_output)?;
        self.create_tool(spec);
        self.attempts.last()
    }

    /// Full tool creation pipeline: validate → register.
    ///
    /// This is the main entry point. Validates the spec, and if it passes,
    /// registers it in the created tools map. Returns the creation attempt.
    pub fn create_tool(&mut self, spec: ToolSpec) -> CreationAttempt {
        let timestamp = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .map(|d| d.as_secs())
            .unwrap_or(0);

        let attempt_num = self.attempts.len() + 1;

        // Run validation
        let validation = self.validate_spec(&spec);

        let attempt = CreationAttempt {
            attempt: attempt_num,
            quality: validation.quality,
            success: validation.passed,
            errors: validation.errors.clone(),
            timestamp,
            spec: spec.clone(),
        };

        if validation.passed {
            self.created_tools.insert(spec.name.clone(), spec);
            self.next_id += 1;
            tracing::info!(
                event = "tool_created",
                name = %attempt.spec.name,
                quality = %attempt.quality,
                "Created and registered new tool"
            );
        } else {
            tracing::warn!(
                event = "tool_creation_failed",
                name = %spec.name,
                errors = ?validation.errors,
                "Tool creation failed validation"
            );
        }

        self.attempts.push(attempt.clone());
        attempt
    }

    /// Refine a failed tool spec (for retry loop).
    ///
    /// Returns a new spec with error information appended as comments
    /// so the model can read the errors and fix its code.
    pub fn format_refinement_prompt(&self, attempt: &CreationAttempt) -> String {
        let mut prompt = format!(
            "The tool '{}' failed validation (attempt {}, quality: {:.2}).\nErrors:\n",
            attempt.spec.name,
            attempt.attempt,
            attempt.quality,
        );
        for err in &attempt.errors {
            prompt.push_str(&format!("- {}\n", err));
        }
        prompt.push_str("\nOriginal code:\n");
        prompt.push_str(&attempt.spec.code);
        prompt.push_str("\n\nPlease fix the errors and create an improved version:\n");
        prompt.push_str("[tool_create]\n");
        prompt.push_str(&format!("name: {}\n", attempt.spec.name));
        prompt.push_str(&format!("description: {}\n", attempt.spec.description));
        prompt.push_str("---\n");
        prompt.push_str("// Fix the above errors...\n");
        prompt.push_str("[/tool_create]");
        prompt
    }

    /// Create a tool from a spec, with automatic refinement loop.
    ///
    /// On failure, returns the error prompt for the model to retry.
    /// Returns `None` if the spec can't even be parsed.
    pub fn create_with_refinement<F>(
        &mut self,
        spec: ToolSpec,
        generate_fn: F,
    ) -> CreationAttempt
    where
        F: Fn(&str) -> String,
    {
        // First attempt
        let mut attempt = self.create_tool(spec);
        let mut attempts = 1;

        // Refinement loop
        while !attempt.success && attempts < self.config.max_attempts {
            let refinement_prompt = self.format_refinement_prompt(&attempt);
            let model_output = generate_fn(&refinement_prompt);

            if let Some(refined_spec) = ToolSpec::parse_from_output(&model_output) {
                attempt = self.create_tool(refined_spec);
                attempts += 1;
            } else {
                // Model didn't produce a valid tool_create block
                attempt.errors.push("Model did not produce a valid [tool_create] block in refinement".into());
                break;
            }
        }

        attempt
    }

    // ---- Validation ----

    /// Validate a tool specification.
    pub fn validate_spec(&self, spec: &ToolSpec) -> ValidationResult {
        let mut checks = Vec::new();
        let mut errors = Vec::new();
        let mut warnings = Vec::new();
        let mut total_score = 0.0f32;
        let mut check_count = 0;

        // Check 1: Name validity
        let name_valid = self.validate_name(&spec.name);
        checks.push(name_valid.clone());
        if !name_valid.passed {
            errors.push(name_valid.message.clone());
        }
        total_score += name_valid.score;
        check_count += 1;

        // Check 2: Code non-empty and within size
        let code_valid = self.validate_code(&spec.code);
        checks.push(code_valid.clone());
        if !code_valid.passed {
            errors.push(code_valid.message.clone());
        }
        total_score += code_valid.score;
        check_count += 1;

        // Check 3: Description non-empty
        let desc_valid = if spec.description.is_empty() {
            warnings.push("Tool has no description".into());
            ValidationCheck {
                name: "description".into(),
                passed: true,
                score: 0.5,
                message: "Description is empty (warning)".into(),
            }
        } else {
            ValidationCheck {
                name: "description".into(),
                passed: true,
                score: 1.0,
                message: "Description provided".into(),
            }
        };
        checks.push(desc_valid);
        check_count += 1;

        // Check 4: Structural validity (balanced braces, no obviously broken code)
        let structural = self.validate_structure(&spec.code);
        checks.push(structural.clone());
        if !structural.passed {
            errors.push(structural.message.clone());
        }
        total_score += structural.score;
        check_count += 1;

        // Check 5: Safety check (no forbidden operations)
        let safety = self.validate_safety(&spec.code);
        checks.push(safety.clone());
        if !safety.passed {
            errors.push(safety.message.clone());
        }
        total_score += safety.score;
        check_count += 1;

        // Check 6: Syntax validation (if enabled)
        if self.config.validate_syntax {
            let syntax = self.validate_syntax(&spec.code);
            checks.push(syntax.clone());
            if !syntax.passed {
                errors.push(syntax.message.clone());
            }
            total_score += syntax.score;
            check_count += 1;
        }

        let quality = if check_count > 0 {
            total_score / check_count as f32
        } else {
            0.0
        };

        let passed = errors.is_empty() && quality >= self.config.quality_threshold;

        ValidationResult {
            passed,
            quality,
            checks,
            errors,
            warnings,
        }
    }

    fn validate_name(&self, name: &str) -> ValidationCheck {
        // Must be non-empty
        if name.is_empty() {
            return ValidationCheck {
                name: "name".into(),
                passed: false,
                score: 0.0,
                message: "Tool name cannot be empty".into(),
            };
        }

        // Must be alphanumeric + underscore
        if !name.chars().all(|c| c.is_alphanumeric() || c == '_') {
            return ValidationCheck {
                name: "name".into(),
                passed: false,
                score: 0.0,
                message: format!("Tool name '{}' contains invalid characters (use alphanumeric + underscore)", name),
            };
        }

        // Must not start with digit
        if name.starts_with(|c: char| c.is_ascii_digit()) {
            return ValidationCheck {
                name: "name".into(),
                passed: false,
                score: 0.0,
                message: "Tool name cannot start with a digit".into(),
            };
        }

        // Must not be reserved
        if self.config.reserved_names.contains(&name.to_string()) {
            return ValidationCheck {
                name: "name".into(),
                passed: false,
                score: 0.0,
                message: format!("Tool name '{}' is reserved", name),
            };
        }

        ValidationCheck {
            name: "name".into(),
            passed: true,
            score: 1.0,
            message: "Name is valid".into(),
        }
    }

    fn validate_code(&self, code: &str) -> ValidationCheck {
        if code.trim().is_empty() {
            return ValidationCheck {
                name: "code_empty".into(),
                passed: false,
                score: 0.0,
                message: "Tool code cannot be empty".into(),
            };
        }

        if code.len() > self.config.max_code_bytes {
            return ValidationCheck {
                name: "code_size".into(),
                passed: false,
                score: 0.0,
                message: format!(
                    "Tool code exceeds maximum size ({} > {} bytes)",
                    code.len(),
                    self.config.max_code_bytes
                ),
            };
        }

        ValidationCheck {
            name: "code".into(),
            passed: true,
            score: 1.0,
            message: "Code size valid".into(),
        }
    }

    fn validate_structure(&self, code: &str) -> ValidationCheck {
        let mut brace_depth = 0i32;
        let mut paren_depth = 0i32;
        let mut bracket_depth = 0i32;

        for ch in code.chars() {
            match ch {
                '{' => brace_depth += 1,
                '}' => brace_depth -= 1,
                '(' => paren_depth += 1,
                ')' => paren_depth -= 1,
                '[' => bracket_depth += 1,
                ']' => bracket_depth -= 1,
                _ => {}
            }
            if brace_depth < 0 || paren_depth < 0 || bracket_depth < 0 {
                return ValidationCheck {
                    name: "structure".into(),
                    passed: false,
                    score: 0.1,
                    message: "Unbalanced delimiters in code".into(),
                };
            }
        }

        if brace_depth != 0 || paren_depth != 0 || bracket_depth != 0 {
            return ValidationCheck {
                name: "structure".into(),
                passed: false,
                score: 0.2,
                message: format!(
                    "Unbalanced delimiters: braces={}, parens={}, brackets={}",
                    brace_depth, paren_depth, bracket_depth
                ),
            };
        }

        // Check that there's at least one function definition
        let has_fn = code.contains("fn ") || code.contains("function ") || code.contains("def ");
        let score = if has_fn { 1.0 } else { 0.6 };

        ValidationCheck {
            name: "structure".into(),
            passed: true,
            score,
            message: if has_fn {
                "Structure valid, function found".into()
            } else {
                "Structure valid but no function definition found".into()
            },
        }
    }

    fn validate_safety(&self, code: &str) -> ValidationCheck {
        let forbidden = [
            ("unsafe", "unsafe code block"),
            ("std::fs", "filesystem access"),
            ("std::net", "network access"),
            ("std::process", "process spawning"),
            ("std::thread", "thread creation"),
            ("::spawn", "spawning processes/threads"),
            ("::system(", "system command execution"),
            ("extern ", "external function calls"),
            ("dlopen", "dynamic library loading"),
            ("raw pointer", "raw pointer manipulation"),
            ("transmute", "memory type transmutation"),
        ];

        let lower = code.to_lowercase();
        let mut violations: Vec<String> = Vec::new();

        for (pattern, desc) in &forbidden {
            if lower.contains(pattern) {
                violations.push(desc.to_string());
            }
        }

        if violations.is_empty() {
            ValidationCheck {
                name: "safety".into(),
                passed: true,
                score: 1.0,
                message: "No forbidden operations detected".into(),
            }
        } else {
            ValidationCheck {
                name: "safety".into(),
                passed: false,
                score: 0.0,
                message: format!("Forbidden operations: {}", violations.join(", ")),
            }
        }
    }

    fn validate_syntax(&self, code: &str) -> ValidationCheck {
        // Lightweight syntax check: try to detect common issues
        // In production, this would use the WASM sandbox to compile
        let has_unterminated_string = {
            let mut in_string = false;
            let mut escape = false;
            for ch in code.chars() {
                if escape {
                    escape = false;
                    continue;
                }
                if ch == '\\' {
                    escape = true;
                    continue;
                }
                if ch == '"' {
                    in_string = !in_string;
                }
            }
            in_string // If still in string, it's unterminated
        };

        if has_unterminated_string {
            return ValidationCheck {
                name: "syntax".into(),
                passed: false,
                score: 0.1,
                message: "Unterminated string literal".into(),
            };
        }

        ValidationCheck {
            name: "syntax".into(),
            passed: true,
            score: 1.0,
            message: "Basic syntax check passed".into(),
        }
    }

    // ---- Query ----

    /// Get creation statistics.
    pub fn stats(&self) -> ToolCreationStats {
        let total = self.attempts.len();
        let successful = self.attempts.iter().filter(|a| a.success).count();
        let failed = total - successful;
        let avg_quality = if successful > 0 {
            self.attempts
                .iter()
                .filter(|a| a.success)
                .map(|a| a.quality)
                .sum::<f32>()
                / successful as f32
        } else {
            0.0
        };

        ToolCreationStats {
            total_attempts: total,
            successful,
            failed,
            avg_quality,
            tools_registered: self.created_tools.len(),
        }
    }

    /// Get all attempts for a specific tool name.
    pub fn attempts_for(&self, name: &str) -> Vec<&CreationAttempt> {
        self.attempts
            .iter()
            .filter(|a| a.spec.name == name)
            .collect()
    }
}

