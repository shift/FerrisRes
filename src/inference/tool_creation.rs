/// Record of a single tool creation attempt.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct CreationAttempt {
    /// Attempt number (1-indexed).
    pub attempt: usize,
    /// The spec that was submitted.
    pub spec: ToolSpec,
    /// Quality score achieved.
    pub quality: f32,
    /// Whether this attempt succeeded.
    pub success: bool,
    /// Error messages (if any).
    pub errors: Vec<String>,
    /// Timestamp.
    pub timestamp: u64,
}

// ---------------------------------------------------------------------------
// Tool creation configuration
// ---------------------------------------------------------------------------

/// Configuration for the tool creation pipeline.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct ToolCreationConfig {
    /// Minimum quality score to accept a created tool.
    pub quality_threshold: f32,
    /// Maximum refinement attempts per tool.
    pub max_attempts: usize,
    /// Maximum code length in bytes.
    pub max_code_bytes: usize,
    /// Banned names (reserved tools).
    pub reserved_names: Vec<String>,
    /// Enable syntax validation via WASM sandbox.
    pub validate_syntax: bool,
    /// Enable semantic validation via mirror test.
    pub validate_semantics: bool,
    /// Maximum execution time per test (ms).
    pub max_execution_ms: u64,
}

impl Default for ToolCreationConfig {
    fn default() -> Self {
        Self {
            quality_threshold: 0.7,
            max_attempts: 3,
            max_code_bytes: 4096,
            reserved_names: vec![
                "calm_execute".into(),
                "mirror_test".into(),
                "concept_lookup".into(),
                "learn".into(),
            ],
            validate_syntax: true,
            validate_semantics: true,
            max_execution_ms: 100,
        }
    }
}

