//! Tool Creation Pipeline — Model Generates Its Own Tools
//!
//! Based on Voyager skill library, LATM (LLMs as Tool Makers), and CREATOR.
//! The model encounters a problem it can't solve directly, generates a tool
//! specification, the WasmSandbox validates it, MirrorTest evaluates quality,
//! and if it passes, the tool is registered in the ToolRegistry.
//!
//! Pipeline:
//!   1. Model emits [tool_create] specification (name, params, returns, code)
//!   2. WasmSandbox compiles and validates the code
//!   3. MirrorTest generates test cases and validates I/O
//!   4. If quality passes threshold → register in ToolRegistry
//!   5. If quality fails → feed error back to model for repair
//!   6. Max 3 refinement iterations before giving up

use std::collections::HashMap;

// ---------------------------------------------------------------------------
// Tool specification
// ---------------------------------------------------------------------------

/// A tool specification generated by the model.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct ToolSpec {
    /// Unique name for the tool.
    pub name: String,
    /// Human-readable description.
    pub description: String,
    /// Parameter types, e.g. "input: string, count: number"
    pub parameters: String,
    /// Return type, e.g. "array of objects"
    pub returns: String,
    /// The tool's implementation code (DSL or WASM subset).
    pub code: String,
    /// Category for tool search indexing.
    pub category: String,
    /// Tags for retrieval.
    pub tags: Vec<String>,
}

impl ToolSpec {
    /// Create a minimal tool spec.
    pub fn new(name: impl Into<String>, description: impl Into<String>, code: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            description: description.into(),
            parameters: String::new(),
            returns: String::new(),
            code: code.into(),
            category: "generated".to_string(),
            tags: vec!["auto-generated".into()],
        }
    }

    /// Parse a tool spec from a model's [tool_create] block.
    ///
    /// Expected format:
    /// ```text
    /// [tool_create]
    /// name: parse_csv
    /// description: Parse CSV string into rows
    /// params: input: string
    /// returns: array
    /// category: parser
    /// ---
    /// fn parse_csv(s) { ... }
    /// [/tool_create]
    /// ```
    pub fn parse_from_output(output: &str) -> Option<Self> {
        let start = output.find("[tool_create]")?;
        let end = output.find("[/tool_create]")?;
        let body = &output[start + "[tool_create]".len()..end];

        let mut name = String::new();
        let mut description = String::new();
        let mut parameters = String::new();
        let mut returns = String::new();
        let mut category = "generated".to_string();
        let mut tags = vec!["auto-generated".to_string()];
        let mut code_lines: Vec<String> = Vec::new();
        let mut in_code = false;

        for line in body.lines() {
            let trimmed = line.trim();
            if trimmed.starts_with("---") {
                in_code = true;
                continue;
            }
            if in_code {
                code_lines.push(line.to_string());
            } else if let Some(val) = trimmed.strip_prefix("name:") {
                name = val.trim().to_string();
            } else if let Some(val) = trimmed.strip_prefix("description:") {
                description = val.trim().to_string();
            } else if let Some(val) = trimmed.strip_prefix("params:") {
                parameters = val.trim().to_string();
            } else if let Some(val) = trimmed.strip_prefix("returns:") {
                returns = val.trim().to_string();
            } else if let Some(val) = trimmed.strip_prefix("category:") {
                category = val.trim().to_string();
            } else if let Some(val) = trimmed.strip_prefix("tags:") {
                tags = val.split(',')
                    .map(|t| t.trim().to_string())
                    .filter(|t| !t.is_empty())
                    .collect();
            }
        }

        if name.is_empty() || code_lines.is_empty() {
            return None;
        }

        Some(Self {
            name,
            description,
            parameters,
            returns,
            code: code_lines.join("\n"),
            category,
            tags,
        })
    }
}

// ---------------------------------------------------------------------------
// Validation result
// ---------------------------------------------------------------------------

/// Result of validating a tool spec.
#[derive(Debug, Clone)]
pub struct ValidationResult {
    /// Whether the tool passed validation.
    pub passed: bool,
    /// Overall quality score (0.0–1.0).
    pub quality: f32,
    /// Individual validation checks.
    pub checks: Vec<ValidationCheck>,
    /// Errors encountered during validation.
    pub errors: Vec<String>,
    /// Warnings (non-fatal issues).
    pub warnings: Vec<String>,
}

impl ValidationResult {
    #[allow(dead_code)]
    fn passed() -> Self {
        Self {
            passed: true,
            quality: 1.0,
            checks: vec![],
            errors: vec![],
            warnings: vec![],
        }
    }

    #[allow(dead_code)]
    fn failed(errors: Vec<String>) -> Self {
        Self {
            passed: false,
            quality: 0.0,
            checks: vec![],
            errors,
            warnings: vec![],
        }
    }
}

/// A single validation check.
#[derive(Debug, Clone)]
pub struct ValidationCheck {
    pub name: String,
    pub passed: bool,
    pub score: f32,
    pub message: String,
}

